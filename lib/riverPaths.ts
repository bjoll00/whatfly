// Convert to GeoJSON format for Mapbox
export function riverPathsToGeoJSON() {
  return {
    type: 'FeatureCollection',
    features: ALL_RIVER_PATHS.map(path => ({
      type: 'Feature',
      geometry: {
        type: 'LineString',
        coordinates: path.coordinates
      },
      properties: {
        id: path.id,
        name: path.name,
        riverSystem: path.riverSystem,
        segmentType: path.segmentType,
        difficulty: path.properties.difficulty,
        access: path.properties.access,
        fishSpecies: path.properties.fishSpecies.join(', '),
        popular: path.properties.popular,
        featured: path.properties.featured,
      }
    }))
  };
}

// Get river path by ID
export function getRiverPathById(id: string): RiverPath | undefined {
  return ALL_RIVER_PATHS.find(path => path.id === id);
}

// Get river paths by system
export function getRiverPathsBySystem(systemName: string): RiverPath[] {
  return ALL_RIVER_PATHS.filter(path => path.riverSystem === systemName);
}

// Get popular river paths
export function getPopularRiverPaths(): RiverPath[] {
  return ALL_RIVER_PATHS.filter(path => path.properties.popular);
}

// Get featured river paths
export function getFeaturedRiverPaths(): RiverPath[] {
  return ALL_RIVER_PATHS.filter(path => path.properties.featured);
}
